package entities;

public class Department {
    //Atributo
    private String name;
    
    //Construtor
    public Department(String name) {
        this.name = name;
    }

    //Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
